package dia0409;

public class usuario {
	int id;
	String nome;
	int senha;
	String permissao;
	String usuario;
	
	String logar;
	String cadastar;
	String freqUltiza;
	String pesquisa;
	
	public int getid() {
		return id;
	}
	public void setid(int id) {
		this.id = id;
	}
	public String getnome() {
		return nome;
	}
	public void setnome(String nome) {
		this.nome = nome;
	}
	public int getsenha() {
		return senha;
	}
	public void setsenha(int senha) {
		this.senha = senha;
	}
	public String getpermissao() {
		return permissao;
	}
	public void setpermissao(String permissao) {
		this.permissao = permissao;
	}
	public String getusuario() {
		return usuario;
	}
	public void setusuario(String usuario) {
		this.usuario = usuario;
	}
	public String getlogar() {
		return logar;
	}
	public void setlogar(String logar) {
		this.logar = logar;
	}
	public String getcadastar() {
		return cadastar;
	}
	public void setcadstar(String cadastar) {
		this.cadastar = cadastar;
	}
	public String getfreqUltizar() {
		return freqUltiza;
	}
	public void setfreqUltiza(String freqUltiza) {
		this.freqUltiza = freqUltiza;
	}
	public String getpesquisa() {
		return pesquisa;
	}
	public void setpesquisa(String pesquisa) {
		this.pesquisa = pesquisa;
	}
	
	
	
	
	

}
